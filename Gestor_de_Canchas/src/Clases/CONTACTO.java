public class CONTACTO {
    
    private String email;
    private String telf;

    public CONTACTO(String email, String telf) {
        this.email = email;
        this.telf = telf;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTelf() {
        return telf;
    }

    public void setTelf(String telf) {
        this.telf = telf;
    }
    
    
    
}
