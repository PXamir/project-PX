     private void cargarReservas(String dni, DefaultTableModel tablaReserva) {
        try {
            List<RESERVA> reservas = reservaDAO.getReservasByDNI(dni);
            for (RESERVA reserva : reservas) {
                Object[] row = {reserva.getDNI(), reserva.getDia(), reserva.getHora()};
                tablaReserva.addRow(row);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar las reservas.");
        }
    }
     
     

