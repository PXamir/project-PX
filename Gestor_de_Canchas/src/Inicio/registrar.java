    private void txtDNIKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDNIKeyPressed
        if (txtDNI.getText().equals("Ingrese su DNI")){
            txtDNI.setText("");
            txtDNI.setForeground(Color.black);
        }
        if(String.valueOf(txtPass.getText()).isEmpty()){
            txtPass.setText("Ingrese su Contrase単a");
            txtPass.setForeground(new Color(111, 111, 111));
        }
        if (txtNombre.getText().isEmpty()){
            txtNombre.setText("Ingrese su Nombre");
            txtNombre.setForeground(new Color(111, 111, 111));
        }
    }//GEN-LAST:event_txtDNIKeyPressed
    
    JPanel panel;
    private void btnRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistrarMouseClicked
                
        
                String dni = txtDNI.getText();
                String nombre = txtNombre.getText();
                String password = txtPass.getText();

                USUARIO usuario = new USUARIO(dni, nombre, password);
                try {
                    usuarioDAO.registerUsuario(usuario);
                    JOptionPane.showMessageDialog(panel, "Usuario registrado exitosamente!");
                } catch (SQLException throwables) {
                    throwables.printStackTrace();
                    JOptionPane.showMessageDialog(panel, "Error al registrar el usuario.");
                }
    }//GEN-LAST:event_btnRegistrarMouseClicked

